package io.github.mletkin.numerobis.annotation;

import static java.lang.annotation.ElementType.FIELD;

import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Indicates that a mutator method shall be generated for a field.
 * <p>
 * Mutators are generated by default. The Annotation may be used to use a custom
 * name for the mutator.
 */
@Retention(RetentionPolicy.SOURCE)
@Target(FIELD)
public @interface GenerateMutator {

    /**
     * Sets the name of the mutator method (is not a prefix).
     *
     * @return name of the mutator method
     */
    String name() default "";
}
